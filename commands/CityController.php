	public function findDistrict($name)
	{
		return FederalDistrict::find()->where(["name" => $name])->one();
	}

	public function findRegion($name)
	{
		return Region::find()->where(["name" => $name])->one();
	}

	public function findCity($name)
	{
		return City::find()->where(["name" => $name])->one();
	}

	public function createDistrict($name, $attr = [])
	{
		if (!$model = $this->findDistrict($name)) {
			$model = new FederalDistrict();
			$model->name = $name;
			$model->setAttributes($attr);
			$model->save();
		}
		return $model;
	}

	public function createRegion($name, $attr = [])
	{
		if (!$model = $this->findRegion($name)) {
			$model = new Region();
			$model->name = $name;
			$model->setAttributes($attr);
			$model->save();
		}
		return $model;
	}

	public function createCity($name, $attr = [])
	{
		if (!$model = $this->findCity($name)) {
			$model = new City();
			$model->name = $name;
			$model->setAttributes($attr);
			$model->save();
		}
		return $model;
	}


	public function actionIndex()
	{
		$transaction = \Yii::$app->db->beginTransaction();
		foreach ($this->getRegion() as $item) {
			$district = $this->createDistrict($item["district"]);
			$this->createRegion($item["name"], ["federal_district_id" => $district->id]);
		}

		foreach ($this->getCity() as $item) {
			$region = $this->findRegion($item["region"]);
			echo $item["city"] . "\n";
			$this->createCity($item["city"], ["region_id" => $region->id]);
		}
		$transaction->commit();
	}

